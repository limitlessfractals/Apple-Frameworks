import SwiftUI

struct AFButton: View {
    var title: String
    var body: some View {
        Text(title)
            .font(.title2)
            .fontWeight(.semibold)
            .frame(width: 270, height: 60)
            .background(Color.red)
            .foregroundColor(.white)
            .cornerRadius(10)
    }
}

